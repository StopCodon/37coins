package com._37coins.web;

public class Seller {
	
	private String mobile;
	
	private float price;

	public String getMobile() {
		return mobile;
	}

	public Seller setMobile(String mobile) {
		this.mobile = mobile;
		return this;
	}

	public float getPrice() {
		return price;
	}

	public Seller setPrice(float price) {
		this.price = price;
		return this;
	}

}
